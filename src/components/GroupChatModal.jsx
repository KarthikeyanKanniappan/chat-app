  const [loading, setLoading] = useState(false);

  const handleSearch = async (query) => {
    setSearch(query);
    if (!query) {
      return;
    }

    try {
      setLoading(true);
      const config = {
        headers: {
          Authorization: `Bearer ${localStorage.getItem("app-token")} `,
        },
      };

      const { data } = await axios.get(
        `${env.api}/users/allUsers?search=${search}`,
        config
      );
    } catch (err) {
      alert(err);
    }
  };
